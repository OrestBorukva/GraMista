import { test } from 'node:test';
import assert from 'node:assert/strict';
import { parseKatottgCsv, mapSettlements } from '../lib/etl/katottg';

const HEADER = 'code,name,level,region,full_address,parent_code,district,council,category,category_name,settlement';
const CSV = [
  '﻿' + HEADER,
  // звичайне село
  'UA01020010020030666,Сонячний,4,Автономна Республіка Крим,адреса,UA01020010000048857,Бахчисарайський район,Андріївська сільська рада,С,село,Сонячний',
  // порожня категорія — теж НП (type=null)
  'UA01020010010075540,Андріївка,4,Автономна Республіка Крим,адреса,UA01020010000048857,Бахчисарайський район,Андріївська сільська рада,,,Андріївка',
  // дубль рядка (є у реальному файлі) — не множиться
  "UA07060450230029534,Прип'ять,4,Волинська область,адреса,UA07060450000051170,Ковельський район,Шацька селищна громада,С,село,Прип'ять",
  "UA07060450230029534,Прип'ять,4,Волинська область,адреса,UA07060450000051170,Ковельський район,Шацька селищна громада,С,село,Прип'ять",
  // район у місті — ВИКЛЮЧИТИ
  'UA63120270050099999,Шевченківський район,4,Харківська область,адреса,UA63120270000099998,,,Р,район у місті,',
  // UA32: колонка region бита («Чорнобиль») — область МУСИТЬ бути Київська (з префікса коду)
  'UA32060050010081797,Бровари,4,Чорнобиль,адреса,UA32060050000030591,Броварський район,Броварська міська громада,М,місто,Бровари',
  // Київ: рівень 1, два рядки з ОДНИМ кодом — лишається один, область «м. Київ»
  'UA80000000000093317,Київ,1,Деснянський район,адреса,,,,,,',
  'UA80000000000093317,Київська міська рада,1,Деснянський район,адреса,,,,,,',
  // назва в лапках з комою — справжній CSV-парсер мусить це зʼїсти
  'UA05020010010011111,"Хутір (старий, новий)",4,Вінницька область,адреса,UA05020010000011110,Вінницький район,Тестова громада,С,село,Хутір',
  // рівень 3 (громада) — не НП, пропустити
  'UA46100110000018989,Миколаївська міська громада (Львівська область),3,Львівська область,адреса,UA46100000000023056,Стрийський район,,,,',
].join('\n');

test('mapSettlements: фільтр рівня/категорій, дедуп, бита region-колонка, Київ', () => {
  const rows = mapSettlements(parseKatottgCsv(CSV));
  const byId = new Map(rows.map((r) => [r.katottg, r]));

  // рівень 3 і «район у місті» відкинуто; дубль не подвоївся; Київ один
  assert.equal(rows.length, 6);
  assert.ok(!byId.has('UA63120270050099999'), 'Р (район у місті) виключено');
  assert.ok(!byId.has('UA46100110000018989'), 'рівень 3 виключено');

  const brovary = byId.get('UA32060050010081797');
  assert.equal(brovary?.oblast, 'Київська', 'область — з префікса коду, НЕ з битої колонки region');
  assert.equal(brovary?.raion, 'Броварський район');
  assert.equal(brovary?.hromada, 'Броварська міська громада');
  assert.equal(brovary?.type, 'місто');

  const kyiv = byId.get('UA80000000000093317');
  assert.equal(kyiv?.name, 'Київ', 'рядок «міська рада» відкинуто');
  assert.equal(kyiv?.oblast, 'м. Київ');
  assert.equal(kyiv?.type, 'місто');

  const empty = byId.get('UA01020010010075540');
  assert.equal(empty?.type, null, 'порожня категорія включена з type=null');
  assert.equal(empty?.nameNorm, 'андріївка');

  assert.equal(byId.get('UA05020010010011111')?.name, 'Хутір (старий, новий)', 'лапки з комою');
});
